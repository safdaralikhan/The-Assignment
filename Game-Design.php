   <h5>Background of the study</h5>
               <p>In the present times, video game industry is growing with the very high pace and places high level of emphasis on business expansion. Moreover, now each individual has smart phone which contain several games and other applications. This aspect has developed high level interest of each and every individual and thereby made contribution in the growth as well as development of such gaming industry (Castro Soeiro and et.al., 2016). Now, with the motive to spend time in joyful manner individuals play game on their mobile phones.  Pokémon go game is one of the biggest examples of video game which has generated high level of popularity. Each individual who has smart phone encouraged to download and play such game. In this, people have task to catch Pokémon which are present at different place. In this way, interest level of individuals was developed significantly in the recent past. By considering such aspect it can be said that smart phone and tablets encourage more people to play game.

               </p>
                <h5>Research questions</h5>
               <p>In the present study, research question is focusing on the extent to which tablet and smart phone devices encourages everyone to play game. Hence, from investigation it has been assessed that use of mobile phones in plying games are increased with the very high pace. Moreover, individual can play game from every place which in turn also motivates them towards this. Further, now gaming companies design battery saver games with the motive to involve everyone (Castañeda and Cho, 2016). In addition to this, video games build and enhance skills, competency and proficiency level of personnel to a great extent. Videos games are repeatedly played by people develop their patience and perseverance level. Tablet and smart phone users play their favorite game repeatedly. This in turn contributes in their growth and development aspect. Role playing games such as Final Fantasy, Dragon Warrior etc. helps people in making stronger. Along with this, there are several video games are available which can be played by people on their phones such as plants vs Zombies, angry birds etc. All these games develop ability of individuals in relation to forward thinking and strategic planning (Carvalho and Araújo, 2016). Thus, it is another main aspect which influences people to play game and thereby enhance potential.

               </p>
                <p>Along with this, friendship groups are also encouraged to play games with each other who have smart phones. In this way, such games maximize leadership and socialization to the significant level. Now, there are several games which enable people to spend their time with each other. Besides this, there are several online games available to individual who offer opportunity to people to communication with each other through voice and video chat. In addition, to develop mental and creative prowess tablets and mobile phone users are influenced to play game (Ebner and et.al., 2016). Sudoku, Tetris etc. games improve brain functionality of people to the significant level. When, people start to play specific game then their desire in relation to cross each and every step increased with the very high pace (Skill development through video games, 2017). In this way, by solving all the logical deductions individual can enhance mental capabilities to the significant level.

