                Children) necessary power to directly imply court’s orders if as per it, any child is likely to suffer
                significant harms (Child protection in the UK, 2017). The act defines the ‘harm’ as a type of
                ill-treatment or impaired/develpment health. But no specific claims have been mentioned about
