            <h4>FACT-SHEET 3 → PROFESSIONALS WORKING TOGETHER FOR REDUCTION IN ABUSES
            </h4>
            <p>Civil-law and criminal-law are the two broad categories of legislation that cover child-protection. Civil
                law has been further divided into public and private laws. Under public-law risks imposed on children
                are minimised and also the required action which has to be taken when child is found under risk. On the
                other hand, private laws allows the professionals to carry with the family proceedings. The criminal-law
                deals with individuals who offend against children. Keeping all these in mind, NSPCC was formulated in
