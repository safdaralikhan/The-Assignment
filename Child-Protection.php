            <h5>Emotional Abuse
            </h5>
            <p>This type of abuse is usually found out in between the relationship that exists among a child and his/her
                parent/carer. This is instead not an outcome of patterned events or either a specific event. This
                usually occurs when needs such as approval, consistency, affection and security are not met. All these
                come under the category of developmental needs. The manifestation of child abuse could be found out by
                the behavioural, affective, cognitive or either the way of physically-functioning of a child. Some
                common examples include : insecure-attachment, unhappy, lower self-esteem, underachieved education.
            </p>
