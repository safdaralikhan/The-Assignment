                <p>e. Strengths and weaknesses of cost volume profit analysis
                </p>
                <p>Cost volume profit analysis: It can be defined as a technique which is used by business entities to
                    determine their contribution which is remaining after deducting all variable expenses from sales or
                    revenues. The contribution is firstly used to recover all the fixed costs and then the remaining amount
                    is considered as profits for the organisation (Schwienbacher and Larralde, 2012). Main objective of
                    using it is to determine relationship between profit, volume and cost of specific business units. With
                    the help of it ability of an organisation to generate profits could be measured. In other words it could
                    be defined as a planning process which is used by managers for the purpose of forecasting future volume
                    of production, sales, costs and profits which could be acquired in future. There are various strengths
                    and weaknesses of this technique which are as follows: <b>Strengths:</b>
                </p>
                <ul>
                    <li>Cost volume profit analysis help top level executives to formulate strategic decisions for future by
                        helping them to determine value of actual expenses and incomes which has taken places for a specific
                        time period. With the help of it they will be able to make strategies in which they can plan to
                        enhance overall profitability of organisation.
                    </li>
                    <li>With the help of cost volume profit analysis detailed information of organisation’s activities could
                        be gathered. It can guide managers to forecast future sales, profits and losses which may take place
                        in upcoming period of time.
                    </li>
                    <li>It helps to find break even point for the organisation so that it can be determined that when amount
                        of units are required to be manufactured in order to cover all the expenses and investments that are
                        made in business.
                    </li>
                    <li>It is considered as the base of plans that are made for various purposes such as marketing,
                        budgeting etc.
                    </li>
                    <li>As it guides managers to forecast future sales and revenues so it can also help them to make budget
                        according to requirements of organisation.
                    </li>
                    <li>Weaknesses:
                    </li>
                    <ul>
                        <li>Cost volume analysis can result in different human errors because for managers it is not
                            possible to record information in a precise way and if detailed data is recorded in books then
                            it can result in human errors (Shoup, 2017).
                        </li>
                        <li>As it is based upon projection and future is uncertain so it is not possible to trust the
                            projections to make appropriate business decisions.
                        </li>
                        <li>It taken number of units in to consideration to project costs and revenues for future period but
                            it could not be the only driver to forecast the same.
                        </li>
                    </ul>
                </ul>
