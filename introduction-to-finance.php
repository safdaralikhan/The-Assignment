                <p>b. Key Differences between discounted and non-discounted methods of investment appraisal
                </p>
                <p>For any organisation, investment appraisal methods play an important role as they help in measuring the
                    feasibility and viability of a project that is considered as a potential opportunity for the enterprise.
                    Due to this reason, a company may apply various discounted and non-discounted methods of investment
                    appraisal. Discounted techniques of investment appraisal are those which ascertain Future Cash flows by
                    discounting them using a certain figure, usually Weighted Average Cost of Capital (WACC) to determine
                    their present day values. These include calculation of NPV, IRR of the projects (Yescombe, 2013). On the
                    other hand, the Non-Discounted methods of investment appraisal are ones which derive cash flows on their
                    current values. Mostly these include determination of Payback period as well as Accounting Rate of
                    Return. Thus, the key difference between the two is the utilization of discounting factor to derive
                    present value of the given future cash flow figures.
