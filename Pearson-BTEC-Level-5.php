                </p>
                <p>Strategic planning is basically an activity of organisational administration which is used to set high
                    status, resources and energy, to strength operations and to make sure that employess and shareholders
                    are working to achieve common goals (Rothaermel, 2015). There are various types of visions, missions,
                    goals, objective and set competencies are being used to achieve strategical planning. British Airways
                    have five strategic goals to achieve like to be the first choice in airline for long wait premium
                    customers, to deliver excellent services at every point, to grow presence in various cities, to stand on
                    top in London, and to satisfy customers needs and maintain margines by new stream of revenue. To achieve
                    such goals, they have certain missions, visions and objective:- Missions: British Airways always make
                    sure that their customer should fly confidently to any destination and they ensure that together they
                    will behave responsibly to take care in the world they live in. This mission of them does not only
                    represent that they are an airline organization but they are a dutiful corporate company (What Is
                    Strategy and Why Is It Important?. 2017). Visions: There vision is to be the dutiful airlines in the
                    world, that is responsible for customer needs, they want to be the indisputable ruler in the world, and
                    be the esteem airlines in famous cities. Objectives: Main objective of British Airways is that they want
                    their customers to choose them again and again, wants to give premium and good quality services to each
                    customers and to be at the leading position in the airlines sector across the world (Higgins, Omer and
                    Phillips, 2015).
