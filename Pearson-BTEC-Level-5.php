                <p>Strategies are formulated by the organizations so that they can attain their vision and mission in an
                    efficient manner. Business strategies are related to the long term plan and practices that are used by
                    the firm so that their overall growth can be improved. The executives have to focused on the resources
                    when they are implementing any plan so that competitiveness can be achieved (Scholes, 2015). Through
                    these strategies, company will be able to fulfil the demand of their stakeholders so that their
                    financial growth can be enhanced. The present report is based on British Airways which perform their
                    operations in aviation industry. They are formulating their plans so that they can increase the
