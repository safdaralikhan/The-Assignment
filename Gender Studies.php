                    recently, a research of civil unions provided evidence on the facts that almost 79 percent of people
                    valued sexual fidelity against only 50 percent homosexual couples. There is much need of extramarital
                    outlets in homosexual couples than in heterosexual. This factor again votes against the same-sex
                    marriage (Ten Arguments From Social Science Against Same-Sex Marriage, 2019). With such strong and
