                <p>Queer, traditionally used as a a term to denote homosexuality, now refers to both people attracted to
                    individuals of same sex and people whose bodies and sexualities do not conform with dominant norms of
                    society. This term clearly explores disagreements between anatomical sex, gender identity and sexual
                    desires. The debate of same sex-marriage in Australia is quite old. This debate has been prominent for
                    the political agenda. Even after this debate has been subjected to varied opinions from the community
                    and politics, still it is controversial and complex in the country. Queer analysis could clearly be
                    understood by Queer Theory, which introduces the idea of “heteronormativity”, a term that undertakes
                    promotion of heterosexuality as a normal sexual orientation. As far as the debate of same sex marriage
                    is considered, Howard Government in 2004 limited the marriage between a woman and a man. However, in
                    2017, Australian Government passed the bill for same sex marriage. The arguments that were presented
                    were quite concrete on both sides of the debate. While the minority referred to Catholic religion where
                    marriage is restricted to a man and a woman, the majority debated about the equality given to
                    individuals regarding their marriage (Vivar, 2016). However, queer analysis could be a relevant source
                    of understanding the same sex debate. According to the analysis, there are multiple perspective that
                    interrelates this aspect to various other concepts like biological and social. As far as the biology is
                    concerned, the analysis focused on the the atypical combination of X and Y chromosomes which is quite
                    natural for them to be different and have contrast sexual characteristics. Where the social concept is
                    concerned, the analysis helps in understanding the behaviour of individuals. As per various theories, an
                    individuals’ self identity is not born with them but rather constructed through repeated practices. As
                    far as homosexuality is concerned, their likings and sexual preferences are dependant upon their
                    identity which is developed through repetitive behavioural and physical practices preferred by those
                    individuals. For the debate in Australia, it is quite important, that both these aspects be understood
                    by the individuals who are against same-sex marriage and it must be considered as a union of two
                    individuals rather than just man and woman. There must be an understanding of the biological
                    characteristics of these individuals to gain a better understanding of their behaviour. Such
                    understanding would allow them to look past the cultural and societal norms and would allow them to
                    accept these marriages and consider them as a natural activity. Moreover, the right of choice must be
