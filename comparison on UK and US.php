                <h3 class="text-center">Report on A comparison on UK and US Health Care System
                </h3>
                <h5>Introduction</h5>
                <p>Health care system includes a range of organizations, individuals, businesses and resources that provide
                    health care services to the target consumers. Main objective of it is to fulfill the health and care
                    needs of people (Rogers, 2011). Health care system plays a major role in maintaining the health
                    standards of country. In the present report, critical analysis and comparison of health care system of
                    UK and US have been done. This report also highlights the similarities and differences of health care
                    system in between these two countries. Further, quality of services, sources of funding and various
                    implications of health care system will be demonstrated.
                </p>
                <h5>MAIN BODY
</h5
>
                <p>World health organization is a global body that is concerned with maintaining the health standards and
                    public health at the global level. According to world health organization, UK is ranked at 9th level for
                    providing superior quality of health care services. However, US lies on 17th rank for providing prime
                    health care services to its target population (International comparison of health care system, 2004).
                    Further, US is taking significant steps to improve its rank in the categories of health care system. For
                    this aspect, US is increasing its government spending on health care sector. Therefore, both the
                    countries; US and UK are doing intense research and development to develop new medical technologies.
                    This has led to origination of cancer therapy, organ transplantation, artificial organ implantation etc.
                </p>
                <p>Similarities and differences between funding of US and UK government
                </p>
                <p>Main similarity between health care services of UK and US is that both countries take funds from public
                    and private sectors to improve and develop health care sector. Further, government of both countries
                    spend the amount collected from different sectors in bettering health care system, developing medical
                    technology, introducing lifesaving drugs etc. According to OECD, total expenditure of UK on health care
                    system was 8.3% of their GDP whereas, US expenditure was estimated at 15.3% of their GDP. This further
                    states that US is incurring more expenditure on gross total and per capita on the health care. Health
                    care industry of UK gets 85.5% funds from government funding. However, the ratio of private sector
                    contribution is 14.5% only (Comparative analysis of UK and US health care system, 2007). Further, tax
                    paid by public is mostly used by government for improving the health care of UK citizens. Contribution
                    of public sectors in the health care system is more in UK. Therefore, government funding is more on
                    health care rather than private bodies. NHS is a regulatory body of UK that is regulated by government
                    to provide cost efficient health care services to target population. NHS is ranked among world’s best
                    health care system. Therefore, it provides services at minimum cost and most of its services are free.
                    Along with this,it provides access to use health care facilities and resources to all the residents of
                    UK. Private sector contributes more on health care system in US rather than its public sector. There are
                    many private bodies as well as organizations that provide health care facilities. However, cost of
                    service provided by private sector organization is more as compared to public sector. Thereafter,
                    insurance companies of US contributes a significant amount of funds to the development of health care
                    sector. In this aspect, health care system of US is developed through funds generated from the insurance
                    purchased by target population (Gerardi, 2008).
                </p>
                <p>Similarities and differences between quality of health care services provided in US and UK
                </p>
                <p>Since, it has been identified that contribution of government to health care sector is more in UK as
                    compared to US. On the contrary, private sector contributes more in US. NHS provides cost efficient
                    services to the residents of country. However, quality of service provided in US is less effective as
                    most of the services are provided free (Dickinson, 2014). NHS involves trust and local organizations
                    that provide primary health care facilities to poor and mediocre. Thereafter, most of health care
                    services in UK are provided by the private bodies. In addition to this aspect, cost and quality of
                    services in UK is more effective. While, government of US possesses innovative medical technology,
                    highly qualified and experienced staff, and large number of health institutions that help in providing
                    superior quality of services to patients. Thereafter, government of both the countries are trying to
                    reduce the expenditure on health care system upto the sustainable level. Therefore, cost of expenditure
                    on health care should not be more than the contribution in GDP (Flynn, 2013).
                </p>
                <p>Similarity and difference between health care organization of UK and US
                </p>
                <p>Health care in US is provided by different organizations such as private, public and charitable.
                    Therefore, 58% services are provided by public bodies, 21% by trust and rest by the government. However
                    in UK more than 75% services are provided by NHS and rest is provided by private bodies. In UK, proper
                    monitoring of organization in health care system and resource allocation are done by the government
                    bodies. There are different types of trusts that help government in attaining its objective of effective
                    health care services such as primary care, acute trust, ambulance trust and care trust. Here, primary
                    health care trust involves dentists, pharmacist etc that provide primary health services while, acute
                    trust involves NHS and government hospitals etc. Further, ambulance trust includes local bodies that
                    handle emergency situations while care trust involves the organization which provides services to those
                    individuals whose treatment is complex (Burau and Blank, 2006). Hence, it can be said that organization
                    structure of UK is more comprised of government bodies and US structure consist of private
                    organizations.
                </p>
                <h5>CONCLUSION</h5>
                <p>From the above report, it can be concluded that health care is a growing sector of UK and US. Health care
                    of UK is mostly depended on the funds generated by government from taxation while, US health care system
                    relies more on funding through insurance companies and private bodies. Thereafter, US government
                    provides better quality of services as compared to UK government. Therefore, satisfaction level
                    regarding health care services is more in case of US citizens.
                </p>
