                <p>Personal knowledge is referred to as acquiring intellectual information through acquaintance and
                    procedural knowledge is based completely on the process or technique with which a task or activity
                    can be accomplished. On the other hand, propositional knowledge is completely different. It refers
                    to the knowledge which depicts specific claims. For instance, I know that concerned project will be
                    completed in two months. Knowledge basically reflects the understanding ability of a person. It can
                    also be considered as the measure of being aware regarding a topic or subject. The individual that
                    has personal knowledge is said to have propositional knowledge in some form. However, the vice versa
                    situation is not necessary to exist. Several philosophers and researchers have depicted that all the
                    three types of knowledge are interlinked and major epistemology has clear depiction of propositional
                    knowledge. Difficulties in the perspective and examples It is often claimed that propositional
                    knowledge majorly consists of true beliefs which are further supported with respective
                    justifications. Great philosophers have classified propositional knowledge as the kind of ability
                    which provides claims regarding proficiency in a subject. For instance, the project management is a
                    strategic tool which involves all the attributes related with a project irrespective of the area of
                    application. The planning tool requires concerned manager to be able to separate activities
                    according to their priorities and then place them in the time scale. If a person claims that Gantt
                    chart depicts the time scale of a project. Then it definitely refers to a propositional knowledge of
                    the person. Considering another example: when a person states that he is aware about the objectives
                    of plan then that person is actually claiming to posses propositional knowledge. True beliefs can be
                    stated as truths only when they are supported or backed by appropriate evidences. These become a
                    proof that the concerned fact or truth does exists and the fact stated by concerned individual holds
                    certain type of validity. Epistemology is considered as an important part of the series based on
                    philosophy. Great thinkers like Aristotle, Buddha, Plato, Kant, etc. had their philosophies in the
                    epistemology. It can be considered as the study or research which comprises of justifications and
                    knowledge that are aligned with rational beliefs. Any sort of knowledge can be considered as the
                    same with validity and reliability in consideration, when there is justification, truth and belief.
                    Scope of knowledge is also depicted through this understanding. A great difference can be
                    experienced between knowing a concept and having an understanding of the operation or technique
                    which needs to be followed. Furthermore, knowledge is also considered to be known through some
                    relation or metaphorical capabilities. The conceptual knowledge can be utilised in application only
                    when it is supported by certain type of justification. Similar can be considered as the case of
                    propositional knowledge. If a person makes certain claims, then it can be contemplated as truth with
                    help of respective justifications or proofs. Foundational-ism is one of the philosophical thesis
                    which portrays the structuring of justification. One can understand this concept with help of the
